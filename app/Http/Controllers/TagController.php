     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index($tag_name = null)
    {
        if ($tag_name)
            return Tag::where('name', 'like', "%$tag_name%")->orderBy('name')->get()->pluck('name');
        else {
            return Tag::orderBy('name')->get()->pluck('name');
        }
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param \Illuminate\Http\Request $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $validated = $this->validate($request, ['name' => 'required|string|max:100']);

        return Tag::firstOrCreate(['name' => $validated['name']]);
    }

